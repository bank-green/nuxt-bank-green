const countryNames = fullCountriesList
    .map(code => ({
        code,
        // title: t(`COUNTRY_${code}`),
        // FIXME
        title: get(code)
    }))
    .sort((a, b) => (a.title > b.title ? 1 : -1))

const fuse = new Fuse(countryNames, { includeScore: true, keys: ['title'] })

/**
 * Some countries don't have banks, show only those with banks
 */
const topCountries = countryNames
    .filter(country => countriesWithBanks.includes(country.code))
    .map(country => country.code)

export function findCountries(searchName) {
    if (!searchName.trim()) {
        return topCountries
    }

    const lower = searchName.toLowerCase()
    switch (lower) {
        case 'usa':
            return ['US']
        case 'uk':
            return ['GB', 'UA']
    }

    const result = fuse.search(searchName)
    return result.filter(x => x.score < 0.3).map(x => x.item.code)
}
